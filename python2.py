def square(x):
    return x * x

def sum(x, y):
    return x + y

def mul(x, y):
    return x * y

def sub(x, y):
    return x - y

print(f'Square of 2: {square(2)}')

print(f'Sum of 2 and 3: {sum(2, 3)}')

print(f'Mul of 4 and 5: {mul(4, 5)}')

print(f'Sub of 4 and 5: {sub(4, 5)}')

